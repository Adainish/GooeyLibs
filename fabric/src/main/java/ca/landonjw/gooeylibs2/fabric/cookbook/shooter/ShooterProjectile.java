package ca.landonjw.gooeylibs2.fabric.cookbook.shooter;

public class ShooterProjectile {

    private int row = 6;
    private final int col;

    public ShooterProjectile(int col) {
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

    public void move() {
        row--;
    }

}